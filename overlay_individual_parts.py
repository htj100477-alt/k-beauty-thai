print("낱개 이미지 오버레이 적용 시작...")

for fname, lines in translation_map.items():
    file_path = os.path.join(parts_dir, fname)
    if not os.path.exists(file_path):
        continue
    
    img = Image.open(file_path).convert("RGBA")
    W, H = img.size
    
    # 텍스트 라인 수에 따라 박스 높이 동적 계산
    box_h = 45 + (len(lines) * 35)
    overlay = Image.new("RGBA", img.size, (0, 0, 0, 0))
    draw = ImageDraw.Draw(overlay)
    
    box_y = H - box_h
    # 어두운 반투명 박스 그리기
    draw.rectangle([(0, box_y), (W, H)], fill=(0, 0, 0, 195))
    
    y_txt = box_y + 18
    # 1. 메인 타이틀 (연초록색)
    f_title = ImageFont.truetype(font_path, 28)
    draw.text((25, y_txt), lines[0], font=f_title, fill=(80, 225, 160, 255))
    y_txt += 38
    
    # 2. 본문 설명들 (흰색)
    f_desc = ImageFont.truetype(font_path, 22)
    for line in lines[1:]:
        draw.text((25, y_txt), line, font=f_desc, fill=(255, 255, 255, 230))
        y_txt += 32
        
    # 원본 이미지 덮어쓰기
    result = Image.alpha_composite(img, overlay).convert("RGB")
    result.save(file_path, "JPEG", quality=90)
    print(f"  오버레이 적용 완료: {fname}")

print("\n모든 낱개 이미지 오버레이 완료!")
