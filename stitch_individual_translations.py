# 모든 파일 정렬해서 로드
all_files = sorted(glob.glob(os.path.join(parts_dir, "part_*.jpg")))
processed_images = []

for p in all_files:
    size = os.path.getsize(p)
    if size < 5000:
        continue
    
    img = Image.open(p).convert("RGBA")
    fname = os.path.basename(p)
    
    # 번역 데이터가 있는 파트인 경우 하단에 박스 합성
    if fname in translation_map:
        W, H = img.size
        lines = translation_map[fname]
        
        # 텍스트 수에 따라 박스 높이 유동적으로 계산
        box_h = 45 + (len(lines) * 35)
        overlay = Image.new("RGBA", img.size, (0, 0, 0, 0))
        draw = ImageDraw.Draw(overlay)
        
        box_y = H - box_h
        # 하단 반투명 어두운 박스
        draw.rectangle([(0, box_y), (W, H)], fill=(0, 0, 0, 195))
        
        y_txt = box_y + 18
        # 첫 줄은 대타이틀로 연초록색 표시
        f_title = ImageFont.truetype(font_path, 28)
        draw.text((25, y_txt), lines[0], font=f_title, fill=(80, 225, 160, 255))
        y_txt += 38
        
        # 나머지 줄은 일반 설명 흰색 표시
        f_desc = ImageFont.truetype(font_path, 22)
        for line in lines[1:]:
            draw.text((25, y_txt), line, font=f_desc, fill=(255, 255, 255, 230))
            y_txt += 32
            
        img = Image.alpha_composite(img, overlay)
        print(f"  [번역 추가됨] {fname}")
    else:
        print(f"  [원본 유지] {fname}")
        
    processed_images.append(img.convert("RGB"))

# 가로 너비 기준 정의
max_w = max(img.width for img in processed_images)

# 최종 이미지의 높이 계산
total_h = sum(img.height for img in processed_images)
combined = Image.new("RGB", (max_w, total_h), (255, 255, 255))

y = 0
for img in processed_images:
    if img.width != max_w:
        img = img.resize((max_w, int(img.height * max_w / img.width)), Image.LANCZOS)
    combined.paste(img, (0, y))
    y += img.height

combined.save(out_path, "JPEG", quality=88)
print(f"\n🎉 최종 합성 및 저장 완료: {out_path} ({combined.size})")
